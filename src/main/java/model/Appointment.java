package model;

public class Appointment {
	private String aptID;
	private String aptName;
	private String aptPhone;
	private String aptDoctor;
	private String aptCategory;
	private String aptDate;
	private String aptStatus;
	
	public String getAptID() {
		return aptID;
	}
	public void setAptID(String aptID) {
		this.aptID = aptID;
	}
	public String getAptName() {
		return aptName;
	}
	public void setAptName(String aptName) {
		this.aptName = aptName;
	}
	public String getAptPhone() {
		return aptPhone;
	}
	public void setAptPhone(String aptPhone) {
		this.aptPhone = aptPhone;
	}
	public String getAptDoctor() {
		return aptDoctor;
	}
	public void setAptDoctor(String aptDoctor) {
		this.aptDoctor = aptDoctor;
	}
	public String getAptCategory() {
		return aptCategory;
	}
	public void setAptCategory(String aptCategory) {
		this.aptCategory = aptCategory;
	}
	public String getAptDate() {
		return aptDate;
	}
	public void setAptDate(String aptDate) {
		this.aptDate = aptDate;
	}
	public String getAptStatus() {
		return aptStatus;
	}
	public void setAptStatus(String aptStatus) {
		this.aptStatus = aptStatus;
	}
	
	
}
