package model;

public class Doctor {
	private String docID;
	private String docName;
	private String docAge;
	private String docGender;
	private String docCategory;
	private String docPhone;
	private String docEmail;
	private String docPassword;
	private String docStatus;
	
	public String getDocID() {
		return docID;
	}
	public void setDocID(String string) {
		this.docID = string;
	}
	public String getDocName() {
		return docName;
	}
	public void setDocName(String docName) {
		this.docName = docName;
	}
	public String getDocAge() {
		return docAge;
	}
	public void setDocAge(String docAge) {
		this.docAge = docAge;
	}
	public String getDocCategory() {
		return docCategory;
	}
	public void setDocCategory(String docCategory) {
		this.docCategory = docCategory;
	}
	public String getDocStatus() {
		return docStatus;
	}
	public void setDocStatus(String docStatus) {
		this.docStatus = docStatus;
	}
	public String getDocPhone() {
		return docPhone;
	}
	public void setDocPhone(String docPhone) {
		this.docPhone = docPhone;
	}
	public String getDocGender() {
		return docGender;
	}
	public void setDocGender(String docGender) {
		this.docGender = docGender;
	}
	public String getDocEmail() {
		return docEmail;
	}
	public void setDocEmail(String docEmail) {
		this.docEmail = docEmail;
	}
	public String getDocPassword() {
		return docPassword;
	}
	public void setDocPassword(String docPassword) {
		this.docPassword = docPassword;
	}
	
	
}
