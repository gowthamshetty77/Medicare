package model;

public class Receptionist {
	private String recepID;
	private String recepName;
	private String recepAge;
	private String recepGender;
	private String recepPhone;
	private String recepEmail;
	private String recepPassword;
	public String getRecepID() {
		return recepID;
	}
	public void setRecepID(String recepID) {
		this.recepID = recepID;
	}
	public String getRecepName() {
		return recepName;
	}
	public void setRecepName(String recepName) {
		this.recepName = recepName;
	}
	public String getRecepAge() {
		return recepAge;
	}
	public void setRecepAge(String recepAge) {
		this.recepAge = recepAge;
	}
	public String getRecepGender() {
		return recepGender;
	}
	public void setRecepGender(String recepGender) {
		this.recepGender = recepGender;
	}
	public String getRecepPhone() {
		return recepPhone;
	}
	public void setRecepPhone(String recepPhone) {
		this.recepPhone = recepPhone;
	}
	public String getRecepEmail() {
		return recepEmail;
	}
	public void setRecepEmail(String recepEmail) {
		this.recepEmail = recepEmail;
	}
	public String getRecepPassword() {
		return recepPassword;
	}
	public void setRecepPassword(String recepPassword) {
		this.recepPassword = recepPassword;
	}
	
}
